int		ft_atoi(char *str)
{
	int		i;
	int		sign;
	long	res;

	i = 0;
	sign = 1;
	res = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == 43 && str[i + 1] != 45)
		i++;
	if (str[i] == 45)
	{
		sign = -1;
		i++;
	}
	while ((str[i] >= 48) && (str[i] <= 57))
	{
		res = (res + str[i] - 48) * 10;
		i++;
	}
	return ((sign * res) / 10);
}
