char	*ft_strrev(char *str)
{
	int		b;
	int		e;
	char	tmp;

	e = 0;
	b = 0;
	while (str[e] != '\0')
	{
		e++;
	}
	e--;
	while (b < e)
	{
		tmp = str[e];
		str[e] = str[b];
		str[b] = tmp;
		b++;
		e--;
	}
	return (str);
}
