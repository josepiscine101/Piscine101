char	*ft_strcat(char *dest, char *src)
{
	int i;
	int y;

	i = 0;
	y = 0;
	while (dest[y] != '\0')
		y++;
	while (src[i] != '\0')
	{
		dest[y + i] = src[i];
		i++;
	}
	dest[i + y] = '\0';
	return (dest);
}
