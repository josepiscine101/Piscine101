char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int y;

	i = 0;
	y = 0;
	while (dest[y] != '\0')
		y++;
	while (i < nb)
	{
		dest[i + y] = src[i];
		i++;
	}
	dest[y + i] = '\0';
	return (dest);
}
